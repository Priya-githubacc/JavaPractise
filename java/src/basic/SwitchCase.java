package basic;

public class SwitchCase {

	public static void main(String[] args) {
 
		int a=22;
		switch(a) {
		
		default : System.out.println("It's default...");
		
		case 777: System.out.println(""
				+ "Yes 2...");
		break;
		case 2: System.out.println("No 2...");
		case 3: System.out.println("No 3...");
			
		}

		
	}

}
