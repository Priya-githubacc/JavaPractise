package Java_Practise;

public class IncrementANDdecrementOperators {

	public static void main(String[] args) {
		// 
		int a=10;
		int b = ++a;
		System.out.println(b);
		
		// For char's
		char c = 'd';
		++c;
		System.out.println(c);
		
		//
		byte bb=10;
		bb=(byte) (bb+1);
		
		int x=10, y=20;
		y=y++;
		System.out.println(y);

		y=--x;
		System.out.println(x);
		
	}
	

}
